"""Monarch actor for executing SkyPilot tasks on Slurm nodes.

This actor runs on each Monarch worker (one per Slurm node) and provides
endpoints for the controller to discover node IPs and dispatch bash commands.

The controller (generated by MonarchCodeGen) spawns SkyPilotExecutor actors
across the Monarch process mesh and invokes their endpoints to execute
setup and run commands on each node.
"""
import socket
from typing import Dict

from monarch.actor import Actor
from monarch.actor import endpoint

from sky.skylet.log_lib import run_bash_command_with_log


class SkyPilotExecutor(Actor):
    """Executes SkyPilot setup/run commands as a Monarch actor.

    Each instance runs on one Slurm node. The controller dispatches
    commands via the run_command endpoint and discovers node IPs via
    get_ip.
    """

    def __init__(self):
        self._ip = socket.gethostbyname(socket.gethostname())

    @endpoint
    def get_ip(self) -> str:
        """Return the IP address of this node."""
        return self._ip

    @endpoint
    def run_command(
        self,
        script: str,
        env_vars: Dict[str, str],
        log_path: str,
        streaming_prefix: str,
    ) -> int:
        """Run a bash script with logging.

        Args:
            script: Bash script content to execute.
            env_vars: Environment variables to set.
            log_path: Path to write log output.
            streaming_prefix: Colorama-formatted prefix for log lines.

        Returns:
            Exit code of the script.
        """
        return run_bash_command_with_log(
            script,
            log_path,
            env_vars=env_vars,
            stream_logs=True,
            streaming_prefix=streaming_prefix,
        )
